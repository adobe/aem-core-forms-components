package com.adobe.cq.forms.core.components.print.internal.models.v1.form;

import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;

import com.adobe.cq.forms.core.Utils;
import com.adobe.cq.forms.core.components.models.form.Medium;
import com.adobe.cq.forms.core.context.FormsCoreComponentTestContext;
import io.wcm.testing.mock.aem.junit5.AemContext;
import io.wcm.testing.mock.aem.junit5.AemContextExtension;

@ExtendWith(AemContextExtension.class)
public class MediumImplTest {

    private static final String BASE = "/form/medium";
    private static final String CONTENT_ROOT = "/content";
    private static final String PATH_MEDIUM = CONTENT_ROOT + "/medium";

    private final AemContext context = FormsCoreComponentTestContext.newAemContext();

    @BeforeEach
    void setUp() {
        context.load().json(BASE + FormsCoreComponentTestContext.TEST_CONTENT_JSON, CONTENT_ROOT);
    }

    @Test
    void testJSONExport() throws Exception {
        Medium medium = Utils.getComponentUnderTest(PATH_MEDIUM, Medium.class, context);
        Utils.testJSONExport(medium, Utils.getTestExporterJSONPath(BASE, PATH_MEDIUM));
    }
}
